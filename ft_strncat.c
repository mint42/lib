#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*cur;

	cur = s1;
	cur += ft_strlen(s1);
	while (n--)
		*cur++ = *s2++;
	*cur = '\0';
	return (s1);
}
