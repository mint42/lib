#include <stddef.h>

char	*ft_parse_space(char **s, size_t *len)
{
	char	*space;

	if (!(*s))
		return (0);
	space = *s;
	*len = 0;
	while ((*s)[*len] == ' ')
		++(*len);
	*s = *s + *len;
	return (space);
}
