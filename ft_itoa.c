#include <stdlib.h>

char	*ft_itoa(int n)
{
	char	*s;
	long	num;
	long	c;

	s = (char *)malloc(sizeof(char *) * 12);
	num = (long)n;
	n = 0;
	c = 1000000000;
	if (num < 0 && s)
	{
		s[n++] = '-';
		num *= -1;
	}
	while (c && s)
	{
		if (num / c)
		{
			s[n++] = ((num / c) + 48);
			num = num % c;
		}
		c = c / 10;
		s[n] = 0;
	}
	return (s);
}
