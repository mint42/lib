#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char	*cur;

	cur = (unsigned char *)dst;
	while (n--)
		*cur++ = *(unsigned char *)src++;
	return (dst);
}
